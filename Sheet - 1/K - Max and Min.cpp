#include <iostream>
#include <cmath>
using namespace std;
int main()
{
    long long a, b, c;
    cin >> a >> b >> c;
    
    if (min(a,b) > min (a,c)){
        cout << min (a,c) << " ";
    }
    else{
        cout << min (a,b) << " ";
    }
    
    if (max(a,b) > max (a,c)){
        cout << max (a,b);
    }
    else{
        cout << max (a,c);
    }
    
    return 0;
}
