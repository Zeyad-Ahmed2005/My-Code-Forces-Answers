#include <iostream>
#include <cmath>
#include <array>
#include <stdio.h>
#include <string>
#include <cctype>
 
using namespace std;
int main()
{
    char x;
    char result;
    cin >> x;
    
    if ((x >= 'A' && x <= 'Z') || (x >= 'a' && x <= 'z')) {
        if (x >= 'A' && x <= 'Z') {
            result = tolower(x);
            cout << result;
        } else if (x >= 'a' && x <= 'z') {
            result = toupper(x);
            cout << result;
        }
    }
    
    return 0;
}
